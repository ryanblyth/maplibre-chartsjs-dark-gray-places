#!/usr/bin/env node
/**
 * Extracts place centroids from the places-low PMTiles (z0 points) and writes
 * a static JS module: data/placeCentroids.js
 *
 * Usage:  node scripts/extract-place-centroids.js
 */

import { PMTiles } from "pmtiles";
import Protobuf from "pbf";
import { VectorTile } from "@mapbox/vector-tile";
import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = resolve(__dirname, "../data/placeCentroids.js");

const PMTILES_URL =
  "https://data.storypath.studio/pmtiles/places/places_cb_2024_points_acs5_2024_density_z0.pmtiles";

async function main() {
  const pm = new PMTiles(PMTILES_URL);
  const header = await pm.getHeader();
  console.log(
    `PMTiles header: minZoom=${header.minZoom} maxZoom=${header.maxZoom}`
  );

  const centroids = new Map();

  // Fetch tiles at z0–2 to capture all places (lower zoom = fewer tiles, broader coverage)
  for (let z = 0; z <= 5; z++) {
    const maxTile = 1 << z;
    for (let x = 0; x < maxTile; x++) {
      for (let y = 0; y < maxTile; y++) {
        let resp;
        try {
          resp = await pm.getZxy(z, x, y);
        } catch {
          continue;
        }
        if (!resp || !resp.data) continue;
        const tile = new VectorTile(new Protobuf(resp.data));
        for (const layerName of Object.keys(tile.layers)) {
          const layer = tile.layers[layerName];
          for (let i = 0; i < layer.length; i++) {
            const feat = layer.feature(i);
            const geoid = feat.properties?.GEOID;
            if (geoid == null) continue;
            const key = String(geoid).replace(/\D/g, "").padStart(7, "0");
            if (centroids.has(key)) continue;
            const geojson = feat.toGeoJSON(x, y, z);
            if (geojson.geometry?.type === "Point") {
              const [lng, lat] = geojson.geometry.coordinates;
              centroids.set(key, [
                Math.round(lng * 1e6) / 1e6,
                Math.round(lat * 1e6) / 1e6,
              ]);
            }
          }
        }
      }
    }
    console.log(`After z${z}: ${centroids.size} centroids`);
    if (centroids.size >= 31000) break;
  }

  // Build JS module
  const entries = [...centroids.entries()]
    .sort(([a], [b]) => (a < b ? -1 : a > b ? 1 : 0))
    .map(([k, [lng, lat]]) => `"${k}":[${lng},${lat}]`)
    .join(",\n");

  const code = `/** Auto-generated by scripts/extract-place-centroids.js — do not edit */\nexport const placeCentroidsByGeoid = {\n${entries}\n};\n`;
  writeFileSync(OUT_PATH, code, "utf-8");
  console.log(`Wrote ${centroids.size} centroids to ${OUT_PATH}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
